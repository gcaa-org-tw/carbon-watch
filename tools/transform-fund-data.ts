  產業分類: string;
  '溫室氣體排放量（公噸二氧化碳當量）': string;
  淨零目標年: string;
  '2030 年減量目標設定': string;
  'SBTi 承諾': string;
  '>> 雷達圖數據': string;
  '2030年溫室氣體絕對減量目標': string;
  '2030年再生能源使用率目標': string;
  '2030年能源效率進步目標': string;
  '2024年再生能源使用率': string;
  '2022-2024年能源效率進步率': string;
  範疇三及減量策略: string;
  有具體減量策略: string;
  範疇三揭露: string;
  範疇三減量規劃: string;
  近三年能效進步率: string;
  節能目標設定: string;
